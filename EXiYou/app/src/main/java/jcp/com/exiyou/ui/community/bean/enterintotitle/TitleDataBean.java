package jcp.com.exiyou.ui.community.bean.enterintotitle;

import java.util.List;

/**
 * Created by SweetDream on 2017/3/30.
 */
public class TitleDataBean {

    private int id;
    private String name;
    private String total_threads;
    private String photo;
    private String description;
    private List<TypesBean> types;
    private String discuss_group_id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTotal_threads() {
        return total_threads;
    }

    public void setTotal_threads(String total_threads) {
        this.total_threads = total_threads;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public List<TypesBean> getTypes() {
        return types;
    }

    public void setTypes(List<TypesBean> types) {
        this.types = types;
    }

    public String getDiscuss_group_id() {
        return discuss_group_id;
    }

    public void setDiscuss_group_id(String discuss_group_id) {
        this.discuss_group_id = discuss_group_id;
    }
}
